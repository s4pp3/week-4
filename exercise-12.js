function countProfit(shoppers) {
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

  if(shoppers == undefined)
		return "";
  else if(shoppers.length == 0)
		return "[]";
  var result = [];
  for(var i=0;i<listBarang.length;i++){
  	var myobj = {};
  	var barang = listBarang[i][0];
  	var harga = listBarang[i][1];
  	var stock = listBarang[i][2];
  	myobj['product'] = barang;
  	myobj['shoppers'] = [];
  	var leftOver = stock;
  	var totalItem = 0;
  	for(var j=0;j<shoppers.length;j++){
  		var name = shoppers[i].name;
  		var product = shoppers[i].product;
  		var amount = shoppers[i].amount;
  		if(product == barang && amount <= stock){
  			leftOver = stock-amount;
  			totalItem = totalItem + amount;
  			myobj['shoppers'].push(name);
  		}
  	}
  	myobj['leftOver'] = leftOver;
  	myobj['totalProfit'] = totalItem*harga;
  	result.push(myobj);
  }
  return result;
}
console.log(countProfit());

